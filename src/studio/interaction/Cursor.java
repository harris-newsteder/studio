package studio.interaction;

public class Cursor {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // VARIABLES
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     * the real position of the cursor on the canvas
     * x : [0, canvas.getWidth()]
     * y : [0, canvas.getHeight()]
     */
    private double rx = 0;
    private double ry = 0;

    /*
     * the position of the cursor in the view
     * x : [-inf, +inf]
     * y : [-inf, +inf]
     */
    private double vx = 0;
    private double vy = 0;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Cursor() {
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GETTERS & SETTERS
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public double getViewX() {
        return vx;
    }

    public void setViewX(double vx) {
        this.vx = vx;
    }

    public double getViewY() {
        return vy;
    }

    public void setViewY(double vy) {
        this.vy = vy;
    }

    public double getRealX() {
        return rx;
    }

    public void setRealX(double rx) {
        this.rx = rx;
    }

    public double getRealY() {
        return ry;
    }

    public void setRealY(double ry) {
        this.ry = ry;
    }
}
