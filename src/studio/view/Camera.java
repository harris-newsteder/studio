package studio.view;

public class Camera {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // VARIABLES
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     * point that the camera is centered on
     */
    private double cx = 0;
    private double cy = 0;

    /*
     *
     */
    private double tx = 0;
    private double ty = 0;

    /*
     *
     */
    private double zoom = 1;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Camera() {

    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // PUBLIC FUNCTIONS
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void calculateTranslation(double width, double height) {
        tx = cx - (width / (2 * zoom));
        ty = cy - (height / (2 * zoom));
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GETTERS & SETTERS
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public double getZoom() {
        return zoom;
    }

    public double getTranslateX() {
        return tx;
    }

    public double getTranslateY() {
        return ty;
    }

    public double getCenterX() {
        return cx;
    }

    public double getCenterY() {
        return cy;
    }

    public void setCenter(double x, double y) {
        this.cx = x;
        this.cy = y;
    }

    public void setZoom(double zoom) {
        this.zoom = zoom;
    }
}
