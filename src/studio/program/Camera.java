package studio.program;

public class Camera {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     *
     */

    public double x = 0;

    /*
     *
     */
    public double y = 0;

    /*
     *
     */
    public double zoom = 1;

    /*
     *
     */
    public double tx = 0;

    /*
     *
     */
    public double ty = 0;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Camera() {

    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     *
     */
    public void calculateTransform(double canvasWidth, double canvasHeight) {
        tx = x - (canvasWidth / (2 * zoom));
        ty = y - (canvasHeight / (2 * zoom));
    }
}
