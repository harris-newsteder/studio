package studio.program.interaction;

public class Cursor {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     *
     * [0, canvas.getWidth()]
     */
    public double rx = 0;

    /*
     *
     * [0, canvas.getHeight()]
     */
    public double ry = 0;

    /*
     *
     * [-inf, +inf]
     */
    public double vx = 0;

    /*
     *
     * [-inf, +inf]
     */
    public double vy = 0;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     *
     */
    private double sx = 0;

    /*
     *
     */
    private double sy = 0;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Cursor() {

    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     *
     */
    public void beginRealDrag() {
        sx = rx;
        sy = ry;
    }

    /*
     *
     */
    public void beginViewDrag() {
        sx = vx;
        sy = vy;
    }

    /*
     *
     */
    public double getRealDeltaX() {
        return rx - sx;
    }

    /*
     *
     */
    public double getRealDeltaY() {
        return ry - sy;
    }

    /*
     *
     */
    public double getViewDeltaX() {
        return vx - sx;
    }

    /*
     *
     */
    public double getViewDeltaY() {
        return vy - sy;
    }
}
