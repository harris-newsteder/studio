package studio.program;

/*
 *
 */
public final class Variable {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTANTS
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public enum Type {
        UNSET,
        DISCRETE_SIGNAL,
        ANALOG_SIGNAL,
        NUMBER,
        BOOLEAN,
        U8,
        U16,
        U32,
        I8,
        I16,
        I32,
        F32,
        F64
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // VARIABLES
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    private Type type;

    private Object value;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Variable(Type type) {
        this.type = type;
    }

    public Variable(Type type, Object value) {
        this.type = type;
        this.value = value;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GETTERS & SETTERS
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

    public Type getType() {
        return type;
    }

    public void setType(Type type) {
        this.type = type;
    }
}
