package studio.ui;

public final class Mouse {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // VARIABLES
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     * the mouse's real position in the scene
     */
    public int realX = 0;
    public int realY = 0;

    /*
     * the mouse's position in the view, the "world"
     */
    public int viewX = 0;
    public int viewY = 0;

    /*
     * the mouse's position snapped to the grid
     */
    public int gridX = 0;
    public int gridY = 0;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Mouse() {

    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GETTERS & SETTERS
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void setRealPosition(int realX, int realY) {
        this.realX = realX;
        this.realY = realY;
    }

    public void setViewPosition(int viewX, int viewY) {
        this.viewX = viewX;
        this.viewY = viewY;
    }

    public void setGridPosition(int gridX, int gridY) {
        this.gridX = gridX;
        this.gridY = gridY;
    }
}
