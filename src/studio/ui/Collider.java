package studio.ui;

import studio.program.Element;

public class Collider {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // VARIABLES
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     *
     */
    private ActionManager manager = null;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Collider(ActionManager manager) {
        this.manager = manager;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // PUBLIC FUNCTIONS
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public Element check(int x, int y) {
        Element ret = null;

        for (Element e : manager.getProgram().getElements()) {
            if (e.getBody().containsPoint(x, y)) {
                ret = e;
            }
        }

        return ret;
    }
}
